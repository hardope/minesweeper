class MinesweeperAI():
    """
    Minesweeper game player
    """

    def __init__(self, height=8, width=8):

        # Set initial height and width
        self.height = height
        self.width = width

        # Keep track of which cells have been clicked on
        self.moves_made = set()

        # Keep track of cells known to be safe or mines
        self.mines = set()
        self.safes = set()

        # List of sentences about the game known to be true
        self.knowledge = []

    def mark_mine(self, cell):
        """
        Marks a cell as a mine, and updates all knowledge
        to mark that cell as a mine as well.
        """
        self.mines.add(cell)
        for sentence in self.knowledge:
            sentence.mark_mine(cell)

    def mark_safe(self, cell):
        """
        Marks a cell as safe, and updates all knowledge
        to mark that cell as safe as well.
        """
        self.safes.add(cell)
        for sentence in self.knowledge:
            sentence.mark_safe(cell)

    def add_knowledge(self, cell, count):
        self.moves_made.add(cell)
        self.safes.add(cell)
        neighbors = self.get_neighbors(cell)
        cells = [neighbor for neighbor in neighbors if neighbor not in self.safes]

        new_sentence = self.create_sentence(cells, count)
        if new_sentence not in self.knowledge:
            self.knowledge.append(new_sentence)

        self.update_knowledge()

    def get_neighbors(self, cell):
        i, j = cell
        neighbors = [(x, y) for x in range(i - 1, i + 2) for y in range(j - 1, j + 2)
                    if (0 <= x < self.height) and (0 <= y < self.width) and (x, y) != (i, j)]
        return neighbors

    def create_sentence(self, cells, count):
        return {"cells": cells, "count": count}

    def update_knowledge(self):
        new_sentences = []
        for sentence in self.knowledge:
            for temp_sentence in self.knowledge:
                if sentence != temp_sentence:
                    if len(sentence["cells"]) > 0 and len(temp_sentence["cells"]) > 0:
                        if set(sentence["cells"]).issubset(temp_sentence["cells"]):
                            modified_set = set(temp_sentence["cells"]) - set(sentence["cells"])
                            modified_count = temp_sentence["count"] - sentence["count"]
                            if len(modified_set) > 0 and modified_count > -1:
                                new_sentences.append({"cells": modified_set, "count": modified_count})

        for sentence in new_sentences:
            if sentence not in self.knowledge:
                self.knowledge.append(sentence)

    def mark_cells(self):
        safes = []
        mines = []

        for sentence in self.knowledge:
            for cell in sentence["cells"]:
                if cell not in self.safes and cell not in safes:
                    safes.append(cell)
            for cell in sentence["cells"]:
                if cell not in self.mines and cell not in mines:
                    mines.append(cell)

        for temp_safe in safes:
            self.mark_safe(temp_safe)
        for temp_mine in mines:
            self.mark_mine(temp_mine)

    def make_safe_move(self):
        for cell in self.safes:
            if cell not in self.moves_made and cell not in self.mines:
                return cell
        return None

    def make_random_move(self):
        available_moves = []
        for i in range(self.height):
            for j in range(self.width):
                if (i, j) not in self.moves_made and (i, j) not in self.mines:
                    available_moves.append((i, j))

        if available_moves:
            return random.choice(available_moves)
        return None
